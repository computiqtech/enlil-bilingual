def compute_spi(df_input, window):
    """Compute SPI for a given time window (months)."""
    df = pd.DataFrame(df_input)
    df.set_index("YEAR", inplace=True)
    df += 0.001  # ensure all values are positive

    df_rolled = df.rolling(window=window, axis=1).mean()
    df_rolled = df_rolled.dropna(axis=1)

    values = df_rolled.values.flatten()

    shape, loc, scale = gamma.fit(values, floc=0)
    cumulative_probs = gamma.cdf(values, shape, loc, scale)
    spi_values = norm.ppf(cumulative_probs)

    spi_df = pd.DataFrame(
        spi_values.reshape(df_rolled.shape),
        columns=df_rolled.columns,
        index=df_rolled.index
    )
    return spi_df


